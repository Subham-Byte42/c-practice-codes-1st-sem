                                    //Printing the values of an array

#include<stdio.h>
int main(){
    int marks[5]={10,20,30,40,50};
    printf("%d\n",marks[0]);     //prints 10  
    printf("%d\n",marks[1]);     //prints 20
    printf("%d\n",marks[2]);     //prints 30
    printf("%d\n",marks[3]);     //prints 40
    printf("%d\n",marks[4]);     //prints 50
    return 0;
}